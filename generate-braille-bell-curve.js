with (Math) {
    // Parameters
    // Left/right: z-values to display
    // hScale: number of horizontal chars
    // vScale: number of vertical chars
    const left = -3.5
    const right = 3.5
    const hScale = 50
    const vScale = 20
    // charWidth & charHeight are for braille
    const charWidth = 2
    const charHeight = 3
    const getChar = bits => String.fromCharCode(0x2800 + parseInt(bits.join(''), 2))

    const height = []
    for (let x = left; x < right; x += (right - left) / (hScale * charWidth)) {
        height.push((1 / sqrt(2 * PI)) * E ** (-0.5 * x ** 2))
    }

    const high = max(...height)
    const low = min(...height)

    const bits = []
    for (let y = low; y < high; y += (high - low) / (vScale * charHeight)) {
        bits.push(height.map(height => height >= y ? 1 : 0))
    }

    const rows = []
    for (let y = 0; y < (vScale * charHeight); y += charHeight) {
        const row = []
        for (let x = 0; x < (hScale * charWidth); x += charWidth) {
            const char = []
            for (let x1 = charWidth; x1 > 0; x1--) {
                for (let y1 = 0; y1 < charHeight; y1++) {
                    char.push(bits[y + y1][x + x1])
                }
            }
            row.push(getChar(char))
        }
        rows.push(row)
    }

    rows.reverse()

    console.log(rows.map(row => row.join('')).join('\r\n'))
}

